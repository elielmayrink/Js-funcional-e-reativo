// Função pura
//   * Uma função pura é uma função em que o valor de retorno é determinado apenas por
//     seus valores de entrada, sem efeitos colaterais observáveis
// ------------------------------------------------------------------------------------------------
//  função inpura  pq usa valores externos a ela
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓

const valorDePi = 3.14;

function areaCircularImpura(raio) {
  return raio * raio * valorDePi;
}

console.log(areaCircularImpura(10));
//↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑

// tornando a função pura
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓

function areaCircularPura(raio, pi) {
  return raio * raio * pi;
}

console.log(areaCircularPura(20, Math.PI));

